package XiaoxianqiZognqiu2;

public class Body {
	//男孩有房
	private boolean boyHasHouse;
	//男孩有车
	private boolean boyHasCar;
	//男孩的年薪
	private int boySalary;
	//男孩送玫瑰花
	private boolean boyGiveFlower;
	
	public boolean getBoyHasHouse() {
		return boyHasHouse;
	}
	public void setBoyHasHouse(boolean boyHasHouse) {
		this.boyHasHouse = boyHasHouse;
	}
	public boolean getBoyHasCar() {
		return boyHasCar;
	}
	public void setBoyHasCar(boolean boyHasCar) {
		this.boyHasCar = boyHasCar;
	}
	public int getBoySalary() {
		return boySalary;
	}
	public void setBoySalary(int boySalary) {
		this.boySalary = boySalary;
	}
	public boolean getBoyGiveFlower() {
		return boyGiveFlower;
	}
	public void setBoyGiveFlower(boolean boyGiveFlower) {
		this.boyGiveFlower = boyGiveFlower;
	}
	
	
}
